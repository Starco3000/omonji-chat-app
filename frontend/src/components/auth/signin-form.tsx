import { cn } from '@/lib/utils';
import { Button } from '@/components/ui/button';
import { Card, CardContent } from '@/components/ui/card';
import { Input } from '@/components/ui/input';
import { Label } from '../ui/label';
import { z } from 'zod';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import { useAuthStore } from '@/stores/useAuthStore';
import { useNavigate } from 'react-router';

const signInSchema = z.object({
  username: z.string().min(1, 'Tên đăng nhập không hợp lệ'),
  password: z.string().min(6, 'Mật khẩu phải có ít nhất 6 ký tự'),
});

type SignInFormValue = z.infer<typeof signInSchema>;

export function SigninForm({
  className,
  ...props
}: React.ComponentProps<'div'>) {
  const { signIn } = useAuthStore();
  const navigate = useNavigate();

  const {
    register,
    handleSubmit,
    formState: { errors, isSubmitting },
  } = useForm<SignInFormValue>({
    resolver: zodResolver(signInSchema),
  });

  const onSubmit = async (data: SignInFormValue) => {
    const { username, password } = data;
    // Call backend API for SighIn
    await signIn(username, password);
    navigate("/")
  };

  return (
    <div className={cn('flex flex-col gap-6', className)} {...props}>
      <Card className='overflow-hidden p-0 border-border'>
        <CardContent className='grid p-0 md:grid-cols-2'>
          <form className='p-6 md:p-8' onSubmit={handleSubmit(onSubmit)}>
            <div className='flex flex-col gap-6'>
              {/* Header - logo */}
              <div className='flex flex-col items-center text-center gap-2'>
                <a href='/' className='mx-auto block w-fit text-center'>
                  <img src='/logo.svg' alt='Logo' />
                </a>
                <h1 className='text-2xl font-bold'>Chào mừng quay trở </h1>
                <p className='text-muted-foreground text-balance'>
                  Đăng nhập vào tài khoản của bạn để tiếp tục
                </p>
              </div>
              {/* username */}
              <div className='flex flex-col gap-3'>
                <Label htmlFor='username' className='block text-sm '>
                  Username
                </Label>
                <Input
                  type='text'
                  id='username'
                  placeholder='Marcusnguyen'
                  {...register('username')}
                />
                {errors.username && (
                  <p className='text-destructive text-sm'>
                    {errors.username.message}
                  </p>
                )}
              </div>
              {/* password */}
              <div className='flex flex-col gap-3'>
                <Label htmlFor='password' className='block text-sm '>
                  Password
                </Label>
                <Input
                  type='password'
                  id='password'
                  {...register('password')}
                />
                {errors.password && (
                  <p className='text-destructive text-sm'>
                    {errors.password.message}
                  </p>
                )}
              </div>
              {/* SignIn button */}
              <Button
                type='submit'
                className='w-full text-base cursor-pointer'
                disabled={isSubmitting}
              >
                Đăng nhập
              </Button>
            </div>

            <div className='mt-2 text-center text-sm *:[a]:hover:text-primary text-muted-foreground *:[a]:underline *:[a]:underline-offset-4'>
              Chưa có tài khoản?{' '}
              <a href='/signup' className='underline underline-offset-4'>
                Đăng ký
              </a>
            </div>
          </form>
          <div className='bg-muted relative hidden md:block'>
            <img
              src='/placeholder.png'
              alt='Image'
              className='absolute top-1/2 -translate-y-1/2 object-cover'
            />
          </div>
        </CardContent>
      </Card>
      <div className='px-6 text-sm text-balance text-center *:[a]:hover:text-primary text-muted-foreground *:[a]:underline *:[a]:underline-offset-4'>
        Bằng cách tiếp tục, bạn đồng ý với <a href='#'>Điều khoản dịch vụ</a> và{' '}
        <a href='#'>Chính sách bảo mật</a> của chúng tôi.
      </div>
    </div>
  );
}
